package basics;

public class IfElseIf {
	public static void main(String[] args) {
		
		int a=20, b=20;
		
		if(a==b) {
			System.out.println("both a and b values are equal");
		}
		else if(a<b) {
			System.out.println("a value is less than b");
		}
		else {
			System.out.println("a value is greater than b");
		}
		
		
		
	}

}


