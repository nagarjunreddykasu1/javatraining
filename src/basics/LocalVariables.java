package basics;

public class LocalVariables {
	
	
	
	public static void main(String[] args) {
		//local variable
		int a=10;
		System.out.println(a);
		
	}

}


/*
Local Variables:
The local variables are the variables declared inside a method or constructor or block.

The scope of local variable is within that method or constructor or block only.

Access modifiers can not be given for local variables.

There is no defualt value for local variables.





*/