package basics;

public class DoWhile {
	
	public static void main(String[] args) {
		
		int num=1;
		
		do {
			if(num%2==0) {
				System.out.println(num);
			}
			num++;
		}while(num<=20);
		
		int a=1;
		do {
			System.out.println(a);
			a++;
		}while(a<=10);
		
		
		
		
	}

}

